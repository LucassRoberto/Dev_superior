package exercise1Class;

public class Department {
	
	private String name;
	
	public Department () {//criando o construtor padrão vazio
		
	}

	public Department(String name) {// construtor padrão com argumentos
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
